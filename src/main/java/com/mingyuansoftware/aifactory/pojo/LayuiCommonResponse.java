package com.mingyuansoftware.aifactory.pojo;

import com.mingyuansoftware.aifactory.enums.WlCode;

public class LayuiCommonResponse {
    private Integer code;
    private String msg;
    private Integer count;
    private Object data;

    public LayuiCommonResponse() {
    }

    public LayuiCommonResponse(WlCode wlcode, Integer count, Object data) {
        this.code = wlcode.getCode();
        this.msg = wlcode.getInfo();
        this.count = count;
        this.data = data;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

}
