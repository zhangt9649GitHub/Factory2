package com.mingyuansoftware.aifactory.model.dto;

import io.swagger.annotations.ApiModelProperty;

/**
 * 〈一句话功能简述〉<br> 
 * 〈入库查询〉
 *
 * @author EDZ
 * @create 2019/7/10
 * @since 1.0.0
 */
public class StorageAccessDto {
    @ApiModelProperty(value = "入库编号", example = "5588")
    private String storageNumber;

    @ApiModelProperty(value = "入库时间", example = "2018-03-04")
    private String rukuTime;

    @ApiModelProperty(value = "创建人", example = "小王")
    private String name;

    @ApiModelProperty(value = "仓库名称", example = "仓库1")
    private String warehouseName;

    @ApiModelProperty(value = "备注", example = "comment")
    private String comment;

    @ApiModelProperty(value = "类型", example = "盘盈入库")
    private String type;

    public String getStorageNumber() {
        return storageNumber;
    }

    public void setStorageNumber(String storageNumber) {
        this.storageNumber = storageNumber;
    }

    public String getRukuTime() {
        return rukuTime;
    }

    public void setRukuTime(String rukuTime) {
        this.rukuTime = rukuTime;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getWarehouseName() {
        return warehouseName;
    }

    public void setWarehouseName(String warehouseName) {
        this.warehouseName = warehouseName;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
