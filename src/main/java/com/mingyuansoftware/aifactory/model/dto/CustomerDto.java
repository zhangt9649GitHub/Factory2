package com.mingyuansoftware.aifactory.model.dto;

import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;

/**
 * @author 张婷
 * @date 2019/6/29 13:43
 */

@ApiModel(description = "张婷")
public class CustomerDto {

    //客户模糊查询需要的数据

    @ApiModelProperty(value = "客户编号", example = "客户编号")
    private String customerNumber;

    @ApiModelProperty(value = "客户名称", example = "客户名称")
    private String customerName;

    @ApiModelProperty(value = "客户电话", example = "电话")
    private String phone;

    @ApiModelProperty(value = "客户地址", example = "地址")
    private String address;

    @ApiModelProperty(value = "客户备注", example = "备注")
    private String comment;

    public String getCustomerNumber() {
        return customerNumber;
    }

    public void setCustomerNumber(String customerNumber) {
        this.customerNumber = customerNumber;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    @Override
    public String toString() {
        return "CustomerDto{" +
                "customerNumber='" + customerNumber + '\'' +
                ", customerName='" + customerName + '\'' +
                ", phone='" + phone + '\'' +
                ", address='" + address + '\'' +
                ", comment='" + comment + '\'' +
                '}';
    }
}
